<?php

namespace App\Entity;

use App\Repository\CategoryPlaceRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=CategoryPlaceRepository::class)
 */
class CategoryPlace
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $nameCategoryPlace;

    /**
     * @ORM\OneToMany(targetEntity=Place::class, mappedBy="category")
     */
    private $places;

    public function __construct()
    {
        $this->places = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getNameCategoryPlace(): ?string
    {
        return $this->nameCategoryPlace;
    }

    public function setNameCategoryPlace(string $nameCategoryPlace): self
    {
        $this->nameCategoryPlace = $nameCategoryPlace;

        return $this;
    }

    public function __toString()
    {
        return $this->nameCategoryPlace;
    }


    /**
     * @return Collection|Place[]
     */
    public function getPlaces(): Collection
    {
        return $this->places;
    }

    public function addPlace(Place $place): self
    {
        if (!$this->places->contains($place)) {
            $this->places[] = $place;
            $place->setCategory($this);
        }

        return $this;
    }

    public function removePlace(Place $place): self
    {
        if ($this->places->removeElement($place)) {
            // set the owning side to null (unless already changed)
            if ($place->getCategory() === $this) {
                $place->setCategory(null);
            }
        }

        return $this;
    }

}
